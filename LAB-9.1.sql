CREATE TABLE COMPUTER
(
	ROLLNO		INT,
	NAMEE		VARCHAR(50)
)

CREATE TABLE ELECTRICAL
(
	ROLLNO		INT,
	NAMEE		VARCHAR(50)
)


SELECT NAMEE FROM COMPUTER 
UNION
SELECT NAMEE FROM ELECTRICAL

SELECT NAMEE FROM COMPUTER 
UNION ALL
SELECT NAMEE FROM ELECTRICAL

SELECT NAMEE FROM COMPUTER 
INTERSECT
SELECT NAMEE FROM ELECTRICAL

SELECT NAMEE FROM COMPUTER 
EXCEPT
SELECT NAMEE FROM ELECTRICAL

